package ch03;

public class C01Arithmetic {
    public static void main(String[] args) {
        // 산술연산자
        // +, -, *, /, %
        // 피연산자 갯수 : 2개
        // 피연산자 : 정수, 실수, 문자
        // 연산결과 타입 : 피연산자 중 큰타입

        int a = 3;
        int b = 9;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(b%a);
    }
}
