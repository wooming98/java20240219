package c02.lecture.p01variable;

public class C01Variable {
    public static void main(String[] args) {

        // int 타입의 저장공간 마련, 공간의 이름은 a
        int a;

        // 변수에 값 저장(assignment, 할당, 대입)
        a = 10;
        System.out.println(a);
        a = 9;
        System.out.println(a);

        int b;
        b = 100;
        System.out.println(b);

    }
}